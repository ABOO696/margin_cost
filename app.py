last = df.iloc[-1]

close_price = float(last["close"])



def kpi(label: str, cost_col: str, direction: str = "long"):

    if cost_col not in df.columns or pd.isna(last.get(cost_col)):

        return

    cost = float(last[cost_col])

    diff = (close_price - cost) / cost * 100

    if direction == "short":

        diff = -diff

    color = "normal" if diff > 0 else "inverse"

    st.metric(label, f"{cost:,.1f}", f"{diff:+.2f}% vs close", delta_color=color)



col1, col2, col3, col4, col5 = st.columns(5)

