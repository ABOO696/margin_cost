def _highlight(row):

    close = row.get("close", 0)

    styles = []

    for col in show_df.columns:

        if "cost" in col and col != "close":

            try:

                val = float(row[col])

                if close < val:

                    styles.append("background-color: rgba(214,39,40,0.25)")

                else:

                    styles.append("background-color: rgba(44,160,44,0.15)")

            except Exception:

                styles.append("")

        else:

            styles.append("")

    return styles



numeric_cols = [c for c in show_df.columns if show_df[c].dtype in ["float64", "float32"]]

